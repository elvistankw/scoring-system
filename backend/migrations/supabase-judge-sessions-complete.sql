-- Drop the old judge_sessions table if exists
DROP TABLE IF EXISTS judge_sessions CASCADE;

-- Create new judge_sessions table for device-based sessions
CREATE TABLE judge_sessions (
    id SERIAL PRIMARY KEY,
    judge_id INTEGER NOT NULL REFERENCES judges(id) ON DELETE CASCADE,
    device_id VARCHAR(255) NOT NULL, -- UUID generated by frontend
    judge_name VARCHAR(255) NOT NULL,
    judge_code VARCHAR(50) NOT NULL,
    ip_address INET,
    user_agent TEXT,
    browser_fingerprint TEXT, -- Additional device identification
    started_at TIMESTAMP DEFAULT NOW(),
    last_activity TIMESTAMP DEFAULT NOW(),
    expires_at TIMESTAMP DEFAULT (NOW() + INTERVAL '8 hours'),
    ended_at TIMESTAMP NULL,
    is_active BOOLEAN DEFAULT true,
    created_at TIMESTAMP DEFAULT NOW(),
    updated_at TIMESTAMP DEFAULT NOW()
);

-- ============================================================================
-- PART 2: Create indexes for performance
-- ============================================================================

CREATE INDEX idx_judge_sessions_judge_id ON judge_sessions(judge_id);
CREATE INDEX idx_judge_sessions_device_id ON judge_sessions(device_id);
CREATE INDEX idx_judge_sessions_active ON judge_sessions(is_active, expires_at);
CREATE INDEX idx_judge_sessions_last_activity ON judge_sessions(last_activity);

-- Create partial unique index for device locking (only one active session per judge)
-- This replaces the problematic UNIQUE constraint with boolean values
CREATE UNIQUE INDEX idx_unique_active_judge 
ON judge_sessions (judge_id) 
WHERE is_active = true;

-- ============================================================================
-- PART 3: Update scores table to reference judge_sessions
-- ============================================================================

-- Add judge_session_id column if not exists
ALTER TABLE scores ADD COLUMN IF NOT EXISTS judge_session_id INTEGER REFERENCES judge_sessions(id);

-- Add judge_name column if not exists (for denormalization)
ALTER TABLE scores ADD COLUMN IF NOT EXISTS judge_name VARCHAR(255);

-- Add updated_at column for partial score updates
ALTER TABLE scores ADD COLUMN IF NOT EXISTS updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP;

-- Update existing records to have updated_at = submitted_at
UPDATE scores SET updated_at = submitted_at WHERE updated_at IS NULL;

-- Create indexes for scores table
CREATE INDEX IF NOT EXISTS idx_scores_judge_session ON scores(judge_session_id);
CREATE INDEX IF NOT EXISTS idx_scores_updated ON scores(updated_at DESC);

-- ============================================================================
-- PART 4: Fix scores.judge_id foreign key constraint
-- ============================================================================

-- Drop the existing foreign key constraint (if exists)
ALTER TABLE scores DROP CONSTRAINT IF EXISTS scores_judge_id_fkey;

-- Add new foreign key constraint referencing judges table (not users table)
-- This allows device-based judge sessions to submit scores
ALTER TABLE scores 
ADD CONSTRAINT scores_judge_id_fkey 
FOREIGN KEY (judge_id) 
REFERENCES judges(id) 
ON DELETE CASCADE;

