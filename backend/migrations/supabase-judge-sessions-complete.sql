-- Function to automatically end expired sessions
CREATE OR REPLACE FUNCTION cleanup_expired_judge_sessions()
RETURNS INTEGER AS $$
DECLARE
    expired_count INTEGER;
BEGIN
    UPDATE judge_sessions 
    SET is_active = false, 
        ended_at = NOW(),
        updated_at = NOW()
    WHERE is_active = true 
      AND expires_at < NOW();
    
    GET DIAGNOSTICS expired_count = ROW_COUNT;
    RETURN expired_count;
END;
$$ LANGUAGE plpgsql;

-- Function to check if a judge is available for selection
CREATE OR REPLACE FUNCTION is_judge_available(judge_id_param INTEGER)
RETURNS BOOLEAN AS $$
BEGIN
    -- Check if judge exists and is active, and has no active session
    RETURN (
        SELECT 
            CASE 
                WHEN j.is_active = false THEN false
                WHEN EXISTS(
                    SELECT 1 FROM judge_sessions js
                    WHERE js.judge_id = judge_id_param
                      AND js.is_active = true
                      AND js.expires_at > NOW()
                ) THEN false
                ELSE true
            END
        FROM judges j
        WHERE j.id = judge_id_param
    );
END;
$$ LANGUAGE plpgsql;

-- Function to get available judges for selection
CREATE OR REPLACE FUNCTION get_available_judges()
RETURNS TABLE(
    id INTEGER,
    name VARCHAR(255),
    display_name VARCHAR(255),
    code VARCHAR(50),
    is_available BOOLEAN,
    current_device_id VARCHAR(255)
) AS $$
BEGIN
    RETURN QUERY
    SELECT 
        j.id,
        j.name,
        j.display_name,
        j.code,
        (j.is_active AND NOT EXISTS(
            SELECT 1 FROM judge_sessions js 
            WHERE js.judge_id = j.id 
              AND js.is_active = true 
              AND js.expires_at > NOW()
        )) as is_available,
        js.device_id as current_device_id
    FROM judges j
    LEFT JOIN judge_sessions js ON j.id = js.judge_id 
        AND js.is_active = true 
        AND js.expires_at > NOW()
    WHERE j.is_active = true
    ORDER BY j.name;
END;
$$ LANGUAGE plpgsql;

